#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*substr;
	size_t	i;
	size_t	slength;

	slength = ft_strlen(s);
	if (len < 0 || !s)
		return ("");

	substr = (char *) malloc(sizeof (*s) * (len + 1));
	if (!substr)
		return (NULL);
	i = 0;
	while (s[start] && i < len)
		substr[i++] = s[start++];
	substr[i] = '\0';
	return (substr);
}
